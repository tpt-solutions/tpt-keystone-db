//! Rust codegen against a live Keystone node — the Rust-native sibling of
//! `tpt-keystone-canvas/src/bin/tsgen.rs` and `@tpt/sdk-web`'s `bin/typegen.ts`
//! (same source data, `information_schema.columns`, different transport:
//! this one talks the Postgres wire protocol via `KeystoneClient` directly,
//! since `tpt-sdk` doesn't go through the HTTP/JSON bridge the browser SDKs
//! need). Emits one `query_builder::Table` impl per table plus a typed
//! `<Table>Row` struct with a `from_row` decoder, to stdout.
//!
//! Usage:
//!   cargo run --bin tpt-sdk-typegen -- 127.0.0.1:5432 > src/generated_schema.rs

use tpt_sdk::keystone::KeystoneClient;

fn pascal_case(name: &str) -> String {
    name.split(['_', '-'])
        .filter(|p| !p.is_empty())
        .map(|p| {
            let mut c = p.chars();
            match c.next() {
                Some(first) => first.to_uppercase().collect::<String>() + c.as_str(),
                None => String::new(),
            }
        })
        .collect()
}

/// Maps a Keystone/Postgres column type name to a Rust field type. Every
/// field is wrapped `Option<...>` since nullability isn't threaded through
/// this codegen pass (same "best effort at the common scalar types" scope
/// cut `keystone::Row::get_value` already documents).
fn rust_type_for(keystone_type: &str) -> &'static str {
    match keystone_type {
        "int2" | "int4" | "int8" => "i64",
        "float4" | "float8" => "f64",
        "bool" => "bool",
        _ => "String",
    }
}

fn decode_expr_for(keystone_type: &str, idx: usize) -> String {
    match keystone_type {
        "int2" | "int4" | "int8" => format!("row.get_str({idx}).and_then(|s| s.parse::<i64>().ok())"),
        "float4" | "float8" => format!("row.get_str({idx}).and_then(|s| s.parse::<f64>().ok())"),
        "bool" => format!("row.get_str({idx}).map(|s| s == \"t\" || s == \"true\")"),
        _ => format!("row.get_str({idx}).map(|s| s.to_string())"),
    }
}

struct Column {
    name: String,
    data_type: String,
}

#[tokio::main]
async fn main() -> anyhow::Result<()> {
    let addr = std::env::args().nth(1).unwrap_or_else(|| "127.0.0.1:5432".to_string());
    let mut client = KeystoneClient::connect(&addr).await.map_err(|e| anyhow::anyhow!("{e}"))?;

    let result = client
        .query(
            "SELECT table_name, column_name, data_type FROM information_schema.columns \
             WHERE table_schema = 'public' ORDER BY table_name, ordinal_position",
        )
        .await
        .map_err(|e| anyhow::anyhow!("{e}"))?;

    let table_i = col_index(&result.columns, "table_name");
    let name_i = col_index(&result.columns, "column_name");
    let type_i = col_index(&result.columns, "data_type");

    let mut tables: Vec<(String, Vec<Column>)> = Vec::new();
    for row in &result.rows {
        let table = row.get_str(table_i).unwrap_or_default().to_string();
        let column = Column {
            name: row.get_str(name_i).unwrap_or_default().to_string(),
            data_type: row.get_str(type_i).unwrap_or_default().to_string(),
        };
        match tables.iter_mut().find(|(t, _)| *t == table) {
            Some((_, cols)) => cols.push(column),
            None => tables.push((table, vec![column])),
        }
    }

    let mut out = String::new();
    out.push_str("// Generated by `tpt-sdk-typegen` — do not hand-edit.\n");
    out.push_str("use tpt_sdk::query_builder::Table;\nuse tpt_sdk::keystone::Row;\n\n");

    for (table, columns) in &tables {
        let struct_name = pascal_case(table);
        let marker_name = format!("{struct_name}Table");
        let cols_list = columns.iter().map(|c| format!("\"{}\"", c.name)).collect::<Vec<_>>().join(", ");

        out.push_str(&format!("pub struct {marker_name};\n"));
        out.push_str(&format!("impl Table for {marker_name} {{\n"));
        out.push_str(&format!("    const NAME: &'static str = \"{table}\";\n"));
        out.push_str(&format!("    const COLUMNS: &'static [&'static str] = &[{cols_list}];\n"));
        out.push_str("}\n\n");

        out.push_str(&format!("#[derive(Debug, Clone, Default)]\npub struct {struct_name} {{\n"));
        for c in columns {
            out.push_str(&format!("    pub {}: Option<{}>,\n", c.name, rust_type_for(&c.data_type)));
        }
        out.push_str("}\n\n");

        out.push_str(&format!("impl {struct_name} {{\n    pub fn from_row(row: &Row) -> Self {{\n        Self {{\n"));
        for (i, c) in columns.iter().enumerate() {
            out.push_str(&format!("            {}: {},\n", c.name, decode_expr_for(&c.data_type, i)));
        }
        out.push_str("        }\n    }\n}\n\n");
    }

    print!("{out}");
    Ok(())
}

fn col_index(columns: &[String], name: &str) -> usize {
    columns.iter().position(|c| c == name).unwrap_or(0)
}
